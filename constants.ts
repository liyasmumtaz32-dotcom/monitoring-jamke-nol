export const getSubjectForDay = (date: Date): SubjectType => {
  const day = date.getDay(); // 0 = Sun, 1 = Mon, ...
  switch (day) {
    case 2: // Tuesday
    case 3: // Wednesday
      return SubjectType.TILAWATI;
    case 4: // Thursday
      return SubjectType.LITERASI;
    case 5: // Friday
      return SubjectType.IBADAH;
    case 6: // Saturday
      return SubjectType.KONSULTASI;
    default:
      return SubjectType.GENERAL;
  }
};
